type MathFunction = (n1: number, n2: number) => number;

const add: MathFunction = (n1, n2) => {
    return n1 + n2
}

const subtract: MathFunction = (n1, n2) => { 
    return n1 - n2
}

const multiply: MathFunction = (n1, n2) => {
    return n1 * n2
}

const division: MathFunction = (n1, n2) => {
    return n1 / n2
}